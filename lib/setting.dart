
class Setting {
  static var widthSize;
  static var heightSize;

  static setWidthSize(var a) {
    widthSize = a;
  }

  static setHeightSize(var b) {
    widthSize = b;
  }

  static getWidthSize() {
    return widthSize;
  }

  static getHeightSize() {
    return heightSize;
  }
}
